C     Process: g c~ > u c~ u~ WEIGHTED<=3 @1
C     Process: g c~ > s c~ s~ WEIGHTED<=3 @1
C     Process: g c~ > d c~ d~ WEIGHTED<=3 @1
C     Process: g c~ > b c~ b~ WEIGHTED<=3 @1
C     Process: g s~ > u s~ u~ WEIGHTED<=3 @1
C     Process: g s~ > c s~ c~ WEIGHTED<=3 @1
C     Process: g s~ > d s~ d~ WEIGHTED<=3 @1
C     Process: g s~ > b s~ b~ WEIGHTED<=3 @1
C     Process: g d~ > u d~ u~ WEIGHTED<=3 @1
C     Process: g d~ > c d~ c~ WEIGHTED<=3 @1
C     Process: g d~ > s d~ s~ WEIGHTED<=3 @1
C     Process: g d~ > b d~ b~ WEIGHTED<=3 @1
C     Process: g b~ > u b~ u~ WEIGHTED<=3 @1
C     Process: g b~ > c b~ c~ WEIGHTED<=3 @1
C     Process: g b~ > s b~ s~ WEIGHTED<=3 @1
C     Process: g b~ > d b~ d~ WEIGHTED<=3 @1
